# get statistics for trimmed regions for the publication
rm(list=ls())
library(ggplot2)
library(GenomicRanges)
library(UpSetR)

inFile <- "/home/rstudio/isilon/private/projects/FetalHindbrain/EMseq_FETHB3/output/downstream/EMseq_FETHB2-FETHB3/CpG/DMRs/CTsnv_excluded/withoutBatchCorrection/240711/oligoDesign/trimmed_targets_240731.tsv"

  # RL active genes
  RL_ACTIVE_GENES <- "/home/rstudio/isilon/private/projects/FetalHindbrain/anno/Hendrikse2022_RL_activeGenes"

dt <- format(Sys.Date(),"%y%m%d")

outDir <- sprintf("%s/stats/",dirname(inFile))
outDir <- sprintf("%s/%s", outDir, dt)
if (!dir.exists(outDir)) {
  dir.create(outDir, recursive = TRUE)
}

rankedFile <- sprintf("%s/targetRanking_240731.tsv", dirname(inFile))
oligoSummaryFile <- sprintf("%s/crsAnnotate/250605/oligo_summary_250605.tsv", dirname(inFile))

dat <- read.table(inFile, header = TRUE, sep = "\t", stringsAsFactors = FALSE)
cat(sprintf("Total number of trimmed targets: %d\n", nrow(dat)))

cat("width distribution\n")
print(summary(dat$end-dat$start+1))

cat("source\n")
print(table(dat$source,useNA="always"))

cat("Reading ranked targets from file\n")
ranked <- read.table(rankedFile, header = TRUE, sep = "\t", stringsAsFactors = FALSE)

# plot violin plot of ranked scores
# add sample size beneath each violin
p <- ggplot(ranked, aes(x = source, y = score)) +
  geom_violin() + geom_boxplot(width = 0.05, fill = "white", outlier.shape = NA) +
  theme_minimal() +
  labs(title = "Ranked Scores by Source", x = "Source", y = "Score") +
  theme(axis.text.x = element_text(angle = 45, hjust = 1)) +
    EnvStats::stat_n_text(size=4)
ggsave(sprintf("%s/ranked_scores_violin_plot.pdf", outDir), plot = p, width = 8, height = 6)

cat("Focusing on DMRs overlapping fetal CB H3K27ac peaks\n")
dmr_fetCBenh <- dat[grep("fetalCbEnh",dat$source),]
cat(sprintf("Number of regions overlapping fetal CB H3K27ac peaks: %d\n", nrow(dmr_fetCBenh)))

# file of JASPAR TFBS overlaps with the DMRs that overlap fetal CB H3K27ac peaks
# only. Generated by getJasparTFBS_forTopTargets.sh
jasparOL <- sprintf("%s/../jaspar_overlaps.bed", outDir)
tfbs <- read.table(jasparOL, header = FALSE, sep = "\t", stringsAsFactors = FALSE)
cat(sprintf("Read %i JASPAR TFBS overlaps with DMRs\n", nrow(tfbs)))
tfbs_nondup <- tfbs[!duplicated(tfbs[,7:10]),]
cat(sprintf("After removing duplicates, %i JASPAR TFBS overlaps with DMRs\n", nrow(tfbs_nondup)))

ame <- "/home/rstudio/isilon/private/projects/FetalHindbrain/EMseq_FETHB3/output/downstream/EMseq_FETHB2-FETHB3/CpG/DMRs/CTsnv_excluded/withoutBatchCorrection/240711/SVZ_diff_hypo_AME_activeInRL_HOCOMOCOv12_240712/ame.tsv"
ame_dat <- read.table(ame, header = TRUE, sep = "\t", stringsAsFactors = FALSE)
dpos <- regexpr("\\.", ame_dat$motif_ID)
tf <- substr(ame_dat$motif_ID, 1,dpos-1)
cat(sprintf("Found %i TFs in AME results\n", length(tf)))
genes2keep <- tf

# filter JASPAR TFBS overlaps for RL active genes
tfbs_nondup <- tfbs_nondup[tfbs_nondup[,7] %in% genes2keep,]
cat(sprintf("After filtering for RL active genes, %i JASPAR TFBS overlaps with DMRs\n", nrow(tfbs_nondup)))


# count frequency of each TF, sort decreasing. Plot a barplot of the top 20 TFs.
tf_freq <- table(tfbs_nondup[,7])
tf_freq <- sort(tf_freq, decreasing = TRUE)
top_tf <- head(tf_freq, 30)
cat("Top 30 TFs in JASPAR TFBS overlaps with DMRs:\n")
print(top_tf)   
# plot barplot of top 20 TFs, plot in descending order of frequency. plot as 
# horizontal barplot with TFs on y-axis and frequency on x-axis.
top_tf <- as.data.frame(top_tf)
p <- ggplot(top_tf, aes(x = reorder(Var1, Freq), y = Freq)) +
    geom_bar(stat = "identity") +
    coord_flip() +
    labs(title = "Top 30 TF binding sites in DMRs overlapping fetal CB H3K27ac peaks",
         x = "TF", y = "Number of TF binding sites in hit loci") +
    theme_minimal(base_size = 20)
ggsave(sprintf("%s/top_30_TFs_in_JASPAR_TFBS_overlaps_with_DMRs.pdf", outDir), plot = p, width = 8, height = 9)


cat("Reading oligo summary from file\n")
oligos <- read.table(oligoSummaryFile, header = TRUE, sep = "\t", stringsAsFactors = FALSE)

cat("Keep the ones overlapping dmr_fetCBenh\n")
oligos_GR <- GRanges(
    seqnames = oligos$seqnames,
    ranges = IRanges(start = oligos$start, end = oligos$end),
    strand = oligos$strand,
    name=oligos$id
)
# write bed file of the oligos
oligos_bed <- sprintf("%s/oligos.bed", outDir)
write.table(as.data.frame(oligos_GR), oligos_bed,
            col.names = FALSE, row.names = FALSE, quote = FALSE, sep = "\t"
)


dmr_fetCBenh_GR <- GRanges(
    seqnames = dmr_fetCBenh$seqnames,
    ranges = IRanges(start = dmr_fetCBenh$start, end = dmr_fetCBenh$end),
    strand = dmr_fetCBenh$strand
)

dat_GR <- GRanges(
    seqnames = dat$seqnames,
    ranges = IRanges(start = dat$start, end = dat$end),
    strand = dat$strand
)
x <- subsetByOverlaps(oligos_GR, dat_GR)
y <- subsetByOverlaps(oligos_GR, dmr_fetCBenh_GR)

