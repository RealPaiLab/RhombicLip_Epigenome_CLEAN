ol1 <-  findOverlaps(gr1.hiconf, dmr_GR)
df1.hiconf <- as.data.frame(gr1.hiconf[queryHits(ol1)])
dmr1 <- cbind(df1.hiconf, dmrs[subjectHits(ol1),])
dmr1 <- dmr1[!duplicated(dmr1),]

ol2 <- findOverlaps(gr2.hiconf, dmr_GR)
df2.hiconf <- as.data.frame(gr2.hiconf[queryHits(ol2)])
dmr2 <- cbind(df2.hiconf, dmrs[subjectHits(ol2),])
dmr2 <- dmr2[!duplicated(dmr2),]

dmr <- rbind(dmr1, dmr2)
