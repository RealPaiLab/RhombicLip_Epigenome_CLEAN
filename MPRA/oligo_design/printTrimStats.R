                    "N2_N3_k27ac",
                    "isNeurodev",
                    "isG34MB",
                    "G34_mutated",
                    "ol_hars")

outFile <- sprintf("%s/oligo_summary_upSet_%s.pdf", outDir, dt)
pdf(outFile,width=10,height=6)
upset(as.data.frame(oligos2), 
      sets = cols2show,
      order.by = "freq",
