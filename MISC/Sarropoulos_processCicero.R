cat("Reading the rhombic lip DMRs now\n")
cat("-----------------------------------\n")
dmrs <- read.delim(CPG_DMR_FILE,sep="\t", header=TRUE)
dmr_GR <- GRanges(dmrs$chr, IRanges(dmrs$start, dmrs$end))

cat("Find overlaps with connections in general\n")
idx1 <- subjectHits(findOverlaps(gr1, dmr_GR))
idx2 <- subjectHits(findOverlaps(gr2, dmr_GR))
idx <- union(idx1, idx2)
cat(sprintf("Total %s DMRs linked to connections\n", prettyNum(length(idx), big.mark = ",")))

