cat("Reading Cicero results for rhombic lip cells\n")
fName <- sprintf("%s/ciceroRLOnly_passCutoff.Rdata", inDir)
load(fName)
conns <- cicero.good; rm(cicero.good)

cat(sprintf("Total %s connections\n", prettyNum(nrow(conns), big.mark = ",")))

# create a data.frame of TSS sites of genes
gene.gr <- import(geneFile, format="gtf")
gene.gr <- subset(gene.gr, gene_type == "protein_coding" & type == "transcript")
tss.gr <- promoters(gene.gr, upstream=0, downstream=1)
tss.df <- data.frame(
    "chr" = seqnames(tss.gr),
    "start" = start(tss.gr),
    "end" = end(tss.gr),
    "gene_id" = tss.gr$gene_id,
    "gene_name" = tss.gr$gene_name
)
tss.df <- tss.df[!duplicated(tss.df$gene_id),]
colnames(tss.df)[5] <- "gene"
cat(sprintf("Created TSS data.frame with %s unique genes\n", prettyNum(nrow(tss.df), big.mark = ",")))

# compute overlap of each of the peaks in the Cicero connections with the TSS sites, and annotate the peaks with gene names.
tmp <- do.call("rbind", strsplit(conns$Peak1, split="-"))
gr1 <- makeGRangesFromDataFrame(data.frame(
    "chr" = tmp[,1],
    "start" = as.numeric(tmp[,2]),
    "end" = as.numeric(tmp[,3])
))
tmp <- do.call("rbind", strsplit(as.character(conns$Peak2), split="-"))
gr2 <- makeGRangesFromDataFrame(data.frame(
    "chr" = tmp[,1],
    "start" = as.numeric(tmp[,2]),
    "end" = as.numeric(tmp[,3])
))
ov1 <- findOverlaps(gr1, tss.gr)
ov2 <- findOverlaps(gr2, tss.gr)
conns$gene1 <- NA
conns$gene2 <- NA
conns$gene1[queryHits(ov1)] <- tss.gr$gene_name[subjectHits(ov1)]
conns$gene2[queryHits(ov2)] <- tss.gr$gene_name[subjectHits(ov2)]
cat(sprintf("Annotated %i peaks with gene names\n", sum(!is.na(conns$gene1)) + sum(!is.na(conns$gene2))))

# annotate the GRanges with gene names.
gr1$gene <- conns$gene1
gr2$gene <- conns$gene2

# annotate ATAC peaks with gene names.
cat("reading Sarropoulos ATAC Seurat object\n")
t0 <- Sys.time()
srat <- readRDS(atacFile)
print(Sys.time()-t0)
cat(sprintf("Sarropoulos ATAC: %i cells, %i features\n", ncol(srat), nrow(srat)))

Idents(srat) <- srat$rna_precisest_label
cat("* Finding marker peaks for the RL progenitor cluster...\n")
da_peaks <- FindMarkers(
  object = srat,
  ident.1 = "progenitor_RL",
  ident.2 = NULL,
  min.pct = 0.1,
  test.use = 'wilcox'
)
cat(sprintf("Total peaks = %s; Q < 0.1 = %s\n", 
    prettyNum(nrow(da_peaks), big.mark = ","), 
    prettyNum(sum(da_peaks$p_val_adj < 0.1), big.mark = ","))
)
open_peaks <- rownames(da_peaks)[da_peaks$p_val_adj < 0.1 & da_peaks$avg_log2FC > 0]
cat(sprintf("Total %s peaks open in RL progenitors\n", 
    prettyNum(length(open_peaks), big.mark = ",")))

cat("-----------------------------------\n")
cat("Getting genes upregulated in the rhombic lip\n")
cat("-----------------------------------\n")
# now get the genes upregulated in this file.
cat("Reading gene expression data\n")
rna <- readRDS(rnaFile)
DefaultAssay(rna) <- "SCT"
Idents(rna) <- rna$precisest_label
t0 <- Sys.time()
rl_markers <- FindMarkers(
  object = rna,
  ident.1 = "progenitor_RL",
  ident.2 = NULL,
  min.pct = 0.1,
  test.use = 'wilcox'
)
print(Sys.time()-t0)
cat(sprintf("Total genes tested for DEG = %s; Q < 0.1 = %s\n", 
    prettyNum(nrow(rl_markers), big.mark = ","), 
    prettyNum(sum(rl_markers$p_val_adj < 0.1), big.mark = ","))
)
upreg_genes <- rownames(rl_markers)[rl_markers$p_val_adj < 0.2 & rl_markers$avg_log2FC > 0]
cat(sprintf("Total %s genes upregulated in RL progenitors\n", 
    prettyNum(length(upreg_genes), big.mark = ",")))

# convert Ensembl IDs to HGNC symbols.
gene.gr <- import(geneFile, format="gtf")
gene.gr <- subset(gene.gr, gene_type == "protein_coding" & type == "gene")
gene.df <- data.frame(
    "chr" = seqnames(gene.gr),
    "start" = start(gene.gr),
    "end" = end(gene.gr),
    "gene_id" = gene.gr$gene_id,
    "gene_name" = gene.gr$gene_name
)
gene.df$gene_id <- gsub("\\..*", "", gene.df$gene_id) 
# convert the names of the upregulated genes from Ensembl IDs to HGNC symbols.
upreg_genes_hgnc <- na.omit(gene.df$gene_name[match(upreg_genes, gene.df$gene_id)])

upreg_gene_idx <- union(
    which(gr1$gene %in% upreg_genes_hgnc), 
    which(gr2$gene %in% upreg_genes_hgnc)
)
gr1.hiconf <- gr1[upreg_gene_idx]
gr2.hiconf <- gr2[upreg_gene_idx]
cat(sprintf("Total %s connections linked to upregulated genes\n", 
    prettyNum(length(upreg_gene_idx), big.mark = ",")))

